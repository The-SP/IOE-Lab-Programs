M=31;
w=0:pi/30:pi;
h=blackman(M);
subplot(2,2,1);plot(w/pi,h);
title('Blackman Window (M=31)');
[H1 w]=freqz(h,1,1024);
subplot(2,2,2);plot(w/pi,abs(H1));
title('Frequency Response for Blackman Window Function');
B=fir1(M-1,1/6); 
[H2 w]=freqz(B,1,1024);
subplot(2, 2, 3);plot(w/pi,abs(H2));
ylabel('Amplitude');
xlabel('Radian Frequency');
title('Frequency Response of FIR filter using Blackman window');
gk=20.*log(abs(H2));
